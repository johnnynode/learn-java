package chapter3.day03;
public class Arith {
	public static void main(String[] args) {
		float num1 = 45.56f;
		int num2 = 152;
		System.out.println("和为： " + num1 + num2);
		System.out.println("差为： " + (num1 - num2));
		System.out.println("积为： " + num1 * num2);
		System.out.println("商为： " + num1 / num2);
	}
}